from aiogram import Router, F
from aiogram.types import CallbackQuery
from keyboards.inline import get_faq_menu

router = Router()


@router.callback_query(F.data == "faq")
async def show_faq(callback: CallbackQuery):
    """Показывает FAQ"""
    faq_text = (
        "❓ <b>FAQ</b>\n\n"
        "<b>Q: Как работает сервис?</b>\n"
        "Мы находим проверенных людей, которые посещают занятия за вас и набирают баллы вместо вас.\n\n"
        "<b>Q: Закрываете ли вы ППА по физре?</b>\n"
        "Нет, ППА мы не закрываем.\n\n"
        "<b>Q: Это безопасно?</b>\n"
        "Да. Данные остаются в безопасности, а на занятиях не проверяют личность.\n\n"
        "<b>Q: Можно ли отменить подписку?</b>\n"
        "Да, в любое время. Мы вернём сумму, пропорциональную оставшимся баллам.\n\n"
        "<b>Q: Что если не получится набрать 100 баллов?</b>\n"
        "Через нас уже прошли десятки студентов, и таких случаев не было. Но если баллы не будут набраны — мы вернём деньги за неиспользованную часть.\n\n"
        "<b>Q: Почему количество мест ограничено?</b>\n"
        "Для безопасности и качества. Слишком много людей = риск. Мы контролируем процесс и гарантируем результат.\n\n"
    )
    
    await callback.message.edit_text(
        faq_text,
        reply_markup=get_faq_menu(),
        parse_mode="HTML"
    )
    await callback.answer()
